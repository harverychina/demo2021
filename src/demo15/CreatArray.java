package demo15;

public class CreatArray {
    public static void main(String[] args) {
        int[] intArray = new int[88];

        System.out.println(intArray[2]);   // 输出第3个元素
        System.out.println(intArray[4]);   // 输出第5个元素

        double[] doubleArray = new double[77];
        System.out.println(doubleArray[6]);
        System.out.println(doubleArray[76]);
    }
}
