package demo2;

public class MathCalc {
    public static void main(String[] args) {
//       普通的字符串是不能运算数学运算的，需要使用数值的类型
        System.out.println(5+6);
        System.out.println(5-6);
        System.out.println(5*6);
        System.out.println(5/6.0);

        System.out.println((1+2+3)*4/5.0);
    }
}
